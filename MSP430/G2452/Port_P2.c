/* ---------- P2_Set_Osc -------------------------------------------- */

/* Set P2 pin to oscillator mode. Argument defines pin to be set.     */

void P2_Set_Osc(unsigned char osc_bit) {

    P2SEL &= osc_bit;
    P2SEL2 |= osc_bit;
  
}

